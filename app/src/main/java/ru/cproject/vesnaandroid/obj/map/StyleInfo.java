public class StyleInfo {

    private int lineColor;
    private int lineWidth;
    private String startIcon;
    private String endIcon;
    private String liftIcon;
    private String escalatorIcon;
    private String parkIcon;
    private int pipeColor;
    private int pipeWidth;

    public String getParkIcon() {
        return parkIcon;
    }

    public void setParkIcon(String parkIcon) {
        this.parkIcon = parkIcon;
    }

    public int getLineColor() {
        return lineColor;
    }

    public void setLineColor(int lineColor) {
        this.lineColor = lineColor;
    }

    public int getLineWidth() {
        return lineWidth;
    }

    public void setLineWidth(int lineWidth) {
        this.lineWidth = lineWidth;
    }

    public String getStartIcon() {
        return startIcon;
    }

    public void setStartIcon(String startIcon) {
        this.startIcon = startIcon;
    }

    public String getEndIcon() {
        return endIcon;
    }

    public void setEndIcon(String endIcon) {
        this.endIcon = endIcon;
    }

    public String getLiftIcon() {
        return liftIcon;
    }

    public void setLiftIcon(String liftIcon) {
        this.liftIcon = liftIcon;
    }

    public String getEscalatorIcon() {
        return escalatorIcon;
    }

    public void setEscalatorIcon(String escalatorIcon) {
        this.escalatorIcon = escalatorIcon;
    }

    public int getPipeColor() {
        return pipeColor;
    }

    public void setPipeColor(int pipeColor) {
        this.pipeColor = pipeColor;
    }

    public int getPipeWidth() {
        return pipeWidth;
    }

    public void setPipeWidth(int pipeWidth) {
        this.pipeWidth = pipeWidth;
    }
}
